async def channel_post_handler(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """–õ–æ–≤–∏–º –ø–æ—Å—Ç—ã –∏–∑ –∫–∞–Ω–∞–ª–∞ –∏ —Å–æ—Ö—Ä–∞–Ω—è–µ–º —Ç–æ–ª—å–∫–æ —Ç–µ, –≥–¥–µ –µ—Å—Ç—å –Ω—É–∂–Ω—ã–µ —Ö—ç—à—Ç–µ–≥–∏."""
    channel_post = update.channel_post
    if not channel_post:
        return

    # –µ—Å–ª–∏ –∑–∞–¥–∞–Ω CHANNEL_ID ‚Äî —Ñ–∏–ª—å—Ç—Ä—É–µ–º –ø–æ –Ω–µ–º—É
    if CHANNEL_ID and channel_post.chat.id != CHANNEL_ID:
        return

    text = channel_post.text or channel_post.caption or ""
    lower = text.lower()

    needed_tags = ["#–∞–∫–∫–æ—Ä–¥—ã", "#–ª–∞–π—Ñ—Ö–∞–∫–∏", "#–≤–¥–æ—Ö–Ω–æ–≤–µ–Ω–∏–µ", "#—Å–µ–º–ø–ª—ã", "#–ø–ª–∞–≥–∏–Ω—ã"]
    if any(tag in lower for tag in needed_tags):
        CHANNEL_POSTS.append(text)
        logger.info("Saved channel post with tags: %s", text[:50])


def run_telegram_bot():
    if not BOT_TOKEN:
        logger.error("BOT_TOKEN not set")
        return

    app_ = ApplicationBuilder().token(BOT_TOKEN).build()

    app_.add_handler(CommandHandler("start", start))
    app_.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, text_handler))

    # —Ö–µ–Ω–¥–ª–µ—Ä –¥–ª—è –ø–æ—Å—Ç–æ–≤ –∏–∑ –∫–∞–Ω–∞–ª–∞
    app_.add_handler(MessageHandler(filters.UpdateType.CHANNEL_POST, channel_post_handler))

    app_.run_polling(allowed_updates=Update.ALL_TYPES, stop_signals=None)


if __name__ == "__main__":
    bot_thread = threading.Thread(target=run_telegram_bot, daemon=True)
    bot_thread.start()

    port = int(os.environ.get("PORT", 10000))
    app.run(host="0.0.0.0", port=port)
